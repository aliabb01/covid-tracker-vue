<template>
  <select style="outline:none;" @change="onChange()" v-model="selected" class="form-select mt-1 block p-3 rounded text-center">
      <option value="0" disabled>Select Country</option>
      <option v-for="country in countries" :key="country.ID" :value="country.ID">
          {{ country.Country }}
      </option>
  </select>
</template>

<script>
export default {
    name: 'CountrySelect',
    props: ['countries'],
    data() {
        return {
            selected: 0
        }
    },
    methods: {
        onChange() {
            const country = this.countries.find((item) => item.ID === this.selected)

            this.$emit('get-country', country)
        }
    }
}
</script>

